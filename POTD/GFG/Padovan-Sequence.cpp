// APPROACH 3

class Solution
{
public:
    int padovanSequence(int n)
    {

        int a = 1, b = 1, c = 1;
        for (int i = 3; i <= n; i++)
        {
            int n = (a + b) % 1000000007;
            a = b;
            b = c;
            c = n;
        }
        return c;
    }
};
// APPROACH 2

class Solution
{
public:
    int padovanSequence(int n)
    {
        if (n == 0 || n == 1 || n == 2)
            return 1;

        vector<int> arr;
        for (int i = 0; i <= 2; i++)
        {
            arr.push_back(1);
        }
        for (int i = 3; i <= n; i++)
        {
            arr.push_back((arr[i - 2] + arr[i - 3]) % 1000000007);
        }
        return arr[arr.size() - 1];
    }
};


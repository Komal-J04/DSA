// APPROACH 1

class Solution
{
public:
    bool threeConsecutiveOdds(vector<int> &arr)
    {
        int n = arr.size();

        for (int i = 0; i < (n - 2); i++)
        {
            if ((arr[i] % 2 == arr[i + 1] % 2) && (arr[i + 1] % 2 == arr[i + 2] % 2) && (arr[i + 2] % 2 == 1))
                return true;
        }

        return false;
    }
};

